import math


def getBMIZscore(pt):
    # returns the age- and gender-specific bmi z-score
    # based on CDC data
    # http://www.cdc.gov/growthcharts/percentile_data_files.htm
    # (downloaded Jan 2013)
    # Z score = [((X/M)^L) - 1]/(L*S)
    
    # make sure the pt instance has the correct properties
    error=[]
    required = ['gender', 'age', 'ht', 'wt']
    for site in required:
        if not hasattr(pt, site):
            error.append(site)
    if len(error) > 0:
        return 'error with the following patient properties: ' + ', '.join(error)
    
    # calculate the BMI
    bmi = pt.bmi()
    # convert age(years) to months
    age = float(pt.age) * 12.0

    # get the appropriate gender-specific data set
    data = []
    if pt.gender in ['m', 'M', 'male', 'Male', 'boy']:
        data = boys
    else:
        data = girls
        
    # convert the age to appropriate ".5" step
    # age in mos
    ageKey = math.floor(age) + 0.5
    # get the lms data from the data set
    lms = data[str(ageKey)]
    l = lms[0]
    m = lms[1]
    s = lms[2]
    
    # make the calculation
    z = ((math.pow(bmi/m, l) - 1) / (l*s))
    return z


def getBMICentile(pt, centile):
    # returns the BMI that is at the 85th percentile;
    # the z score for this percentile is +1.036
    # from the CDC: z-scores correspond exactly to percentiles, e.g., z-scores of -1.881, -1.645, -1.282, -0.674,
    # 0, 0.674, 1.036, 1.282, 1.645, and 1.881 correspond to the 3rd, 5th, 10th, 25th, 50th, 75th, 85th, 90th,
    # 95th, and 97th percentiles, respectively.

    # make sure the pt instance has the correct properties
    centiles = {
        '3': -1.881,
        '5': -1.645,
        '15': -1.036,
        '10': -1.282,
        '25': -0.674,
        '50': 0.0001,
        '75': 0.674,
        '85': 1.036,
        '90': 1.282,
        '95': 1.645,
        '97': 1.881
    }
    error = []
    required = ['gender', 'age']
    for site in required:
        if not hasattr(pt, site):
            error.append(site)
    if len(error) > 0:
        return 'error with the following patient properties: ' + ', '.join(error)

    # convert age(years) to months
    age = float(pt.age) * 12.0
    # convert the age to appropriate ".5" step
    # age in mos
    ageKey = math.floor(age) + 0.5

    # get the appropriate gender-specific data set
    data = []
    if pt.gender in ['m', 'M', 'male', 'Male', 'boy']:
        data = boys
    else:
        data = girls

    # get the lms data from the data set
    lms = data[str(ageKey)]
    l = lms[0]
    m = lms[1]
    s = lms[2]

    z = centiles[centile] if centiles[centile] else 1.036 #default initial value

    # re-arranging the z-score formula to solve for x
    bmi = m * math.pow((z * l * s + 1), (1/l))
    return bmi


